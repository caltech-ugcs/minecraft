def translate(vertices, x, y, z):
    """
    Translate the vertex buffer by the given x, y, and z offsets
    and return the result.
    """
    n = len(vertices)
    vertices = np.reshape(vertices, (n // 3, 3))
    vertices[:,0] += x
    vertices[:,1] += y
    vertices[:,2] += z
    return np.reshape(vertices, (n)).tolist()
